const praise1 = document.querySelector('.praise .icon1')
const praiseNum1 = document.querySelector('.praise .num1')
let hasPraised1 = false
praise1.addEventListener('click', function () {
    hasPraised1 = !hasPraised1
    if (hasPraised1) {
        praise1.style.backgroundImage = `url(../../images/praised.png)`
        praiseNum1.innerHTML = parseInt(praiseNum1.innerText.trim()) + 1
    } else {
        praise1.style.backgroundImage = `url(../../images/praise.png)`
        praiseNum1.innerHTML = parseInt(praiseNum1.innerText.trim()) - 1
    }
})

